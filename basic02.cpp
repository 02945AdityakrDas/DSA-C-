#include<iostream>
using namespace std;
int main(){
    //int data type
    int mango = 5;
    cout<<mango<<endl;
    cout<<"Size of int: "<<sizeof(mango)<<endl;
    //char data type
    char a ='@';
    cout<<a<<endl;
    cout << "Size of char: " << sizeof(a) << endl;
    //bool data type
    bool flag = true;
    cout<<flag<<endl;
    cout << "Size of bool: " << sizeof(flag) << endl;
    //float data type
    float pi = 3.14;
    cout<<pi<<endl;
    cout << "Size of float: " << sizeof(pi) << endl;
    return 0;
}